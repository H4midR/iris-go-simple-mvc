package hfuncs

import "strings"

func FaSafeString(str string) string {
	r := strings.NewReplacer(
		`"`, "",
		`'`, "",
		"`", "",
		`~`, "",
		`!`, "",
		`@`, "",
		`#`, "",
		`$`, "",
		`%`, "",
		`^`, "",
		`&`, "",
		`*`, "",
		`(`, "",
		`)`, "",
		`{`, "",
		`}`, "",
		`[`, "",
		`]`, "",
		`+`, "",
		`=`, "",
		`-`, "",
		`_`, "",
		`|`, "",
		`\`, "",
		`/`, "",
		`;`, "",
		`:`, "",
		`,`, "",
		`<`, "",
		`>`, "",
		`?`, "",
		// حروف فارسی
		"  ", " ",
		"ِ", "",
		"ُ", "",
		"َ", "",
		"ً", "",
		"ٌ", "",
		"ٍ", "",
		"ة", "ه",
		"ي", "ی",
		"ؤ", "و",
		"إ", "ا",
		"أ", "ا",
		"؟", "",
		"ّ", "",
		"ۀ", "ه",
		"ـ", "",
		"«", "",
		"»", "",
		"؛", "",
		",", "",
	)
	return r.Replace(str)
}
